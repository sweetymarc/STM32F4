void OV7725_Reset(void)
{
	BSP_CAMERA_Reset(0);
	HAL_Delay(100);
	BSP_CAMERA_Reset(1);
	HAL_Delay(100);		
	#ifdef DEBUG
		printf("ov7725 reset\r\n");
	#endif
}

void OV7725_Init(void)
{
	uint32_t i;
	BSP_CAMERA_Init();
  OV7725_Reset();
  /* Initialize OV */
	printf("ov id: %x\r\n%x\r\n%x\r\n", BSP_Reg_Get(0x1c),BSP_Reg_Get(0x1d),BSP_Reg_Get(0x0b));
	for(i=0; i<(sizeof(OV_QVGA)/2); i++)
  {
    BSP_Reg_Set(OV_QVGA[i][0], OV_QVGA[i][1]);
		HAL_Delay(1);
  }
	HAL_I2C_DeInit(&hI2C_camera);
	printf("OV7725 finished ok\r\n");
}
